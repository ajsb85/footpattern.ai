DbService dbService = DbService();

Future<bool> isAdmin() async {
  final admin = await dbService.checkAdminStatus();
  return admin; // Assuming 'true' means admin and 'false' means non-admin
}

class _HomeState extends State<Home> with SingleTickerProviderStateMixin {
  final GlobalKey<ScaffoldState> scaffoldKey = GlobalKey<ScaffoldState>();
  late final AnimationController controller;
  late final CurvedAnimation railAnimation;
  bool controllerInitialized = false;
  bool showMediumSizeLayout = false;
  bool showLargeSizeLayout = false;

  int screenIndex = ScreenSelected.component.value;
  late Future<bool> admin;

  @override
  void initState() {
    super.initState();
    controller = AnimationController(
      duration: Duration(milliseconds: transitionLength.toInt() * 2),
      value: 0,
      vsync: this,
    );
    railAnimation = CurvedAnimation(
      parent: controller,
      curve: const Interval(0.5, 1.0),
    );

    // Fetch the admin status in initState
    admin = isAdmin();
  }

  @override
  void dispose() {
    controller.dispose();
    super.dispose();
  }

  void handleScreenChanged(int screenSelected) {
    setState(() {
      screenIndex = screenSelected;
    });
  }

  Widget createScreenFor(
      ScreenSelected screenSelected, bool showNavBarExample, bool isAdmin) {
    if (isAdmin) {
      switch (screenSelected) {
        case ScreenSelected.users:
          return const UserScreen();
        case ScreenSelected.component:
          return Expanded(
            child: StreamBuilder<BluetoothAdapterState>(
              stream: FlutterBluePlus.adapterState,
              initialData: BluetoothAdapterState.unknown,
              builder: (c, snapshot) {
                final adapterState = snapshot.data;
                if (adapterState == BluetoothAdapterState.on) {
                  return const FindDevicesScreen();
                } else {
                  FlutterBluePlus.stopScan();
                  return BluetoothOffScreen(adapterState: adapterState);
                }
              },
            ),
          );
        case ScreenSelected.typography:
          return const ColorPalettesScreen();
        // case ScreenSelected.color:
        //   return const ProfileScreen();
      }
    } else {
      switch (screenSelected) {
        case ScreenSelected.users:
          return const ColorPalettesScreen();
        case ScreenSelected.component:
          return Expanded(
            child: OneTwoTransition(
              animation: railAnimation,
              one: FirstComponentList(
                showNavBottomBar: showNavBarExample,
                scaffoldKey: scaffoldKey,
                showSecondList: showMediumSizeLayout || showLargeSizeLayout,
              ),
              two: SecondComponentList(
                scaffoldKey: scaffoldKey,
              ),
            ),
          );
        case ScreenSelected.typography:
          return const ProfileScreen();
        // case ScreenSelected.color:
          // return const ProfileScreen();
      }
    }
  }

  PreferredSizeWidget createAppBar() {
    return AppBar(
      title: Image.asset(
        'lib/assets/logo.png',
        width: 60,
        height: 60,
      ),
      
    );
  }

  @override
  Widget build(BuildContext context) {
    return FutureBuilder<bool>(
      future: admin,
      builder: (context, snapshot) {
        if (snapshot.connectionState == ConnectionState.done) {
          final adminValue = snapshot.data ?? false;
          return AnimatedBuilder(
            animation: controller,
            builder: (context, child) {
              return NavigationTransition(
                scaffoldKey: scaffoldKey,
                animationController: controller,
                railAnimation: railAnimation,
                appBar: createAppBar(),
                body: createScreenFor(
                  ScreenSelected.values[screenIndex],
                  controller.value == 1,
                  adminValue,
                ),
                navigationRail: NavigationRail(
                  extended: showLargeSizeLayout,
                  destinations: navRailDestinations,
                  selectedIndex: screenIndex,
                  onDestinationSelected: (index) {
                    setState(() {
                      screenIndex = index;
                      handleScreenChanged(screenIndex);
                    });
                  },
                  trailing: Expanded(
                    child: Padding(
                      padding: const EdgeInsets.only(bottom: 20),
                      child: showLargeSizeLayout
                          ? _ExpandedTrailingActions(
                              useLightMode: widget.useLightMode,
                              handleBrightnessChange:
                                  widget.handleBrightnessChange,
                              useMaterial3: widget.useMaterial3,
                              handleMaterialVersionChange:
                                  widget.handleMaterialVersionChange,
                              handleImageSelect: widget.handleImageSelect,
                              handleColorSelect: widget.handleColorSelect,
                              colorSelectionMethod: widget.colorSelectionMethod,
                              imageSelected: widget.imageSelected,
                              colorSelected: widget.colorSelected,
                            )
                          : Text(''),
                    ),
                  ),
                ),
                navigationBar: NavigationBars(
                  onSelectItem: (index) {
                    setState(() {
                      screenIndex = index;
                      handleScreenChanged(screenIndex);
                    });
                  },
                  selectedIndex: screenIndex,
                  isExampleBar: false,
                ),
              );
            },
          );
        } else {
          return Center(
            child:
                CircularProgressIndicator(), // Show a loading indicator while fetching the admin status.
          );
        }
      },
    );
  }
}


